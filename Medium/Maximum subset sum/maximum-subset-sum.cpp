class Solution {

  public:
    long long findMaxSubsetSum(int N, vector<int> A) {
        A.push_back(0);
        int p1 = 0;
        int p2 = 0;
        int c = 0;
        for(int i=0; i<=N; i++){
            c = max(p1+A[i], p2+A[i]);
            p2 = p1;
            p1 = c;
        }
        return p1;
    }
};


