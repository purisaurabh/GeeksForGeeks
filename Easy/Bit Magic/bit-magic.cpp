class Solution {
  public:
    int bitMagic(int n, vector<int> &arr) {
        // code here
        
        int l =0 , r =n-1 ,ans =0 ;
        while(l<=r)
        {
            if(arr[l++]!=arr[r--])
            ans++; 
        }
        if(ans%2==0)
        return ans/2 ;
        return ans/2 + 1 ;
    }
};


